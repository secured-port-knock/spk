package sniffer

import (
	"testing"
)

// FuzzIPv4UDPFragments ensures IPv4/UDP packet unmarshaling does not panic on fragments, Jumbo frames, or strange options.
func FuzzIPv4UDPFragments(f *testing.F) {
	f.Add([]byte{0x45, 0x00, 0x00, 0x1c, 0x00, 0x00, 0x40, 0x00, 0x40, 0x11, 0x00, 0x00, 0x7f, 0x00, 0x00, 0x01, 0x7f, 0x00, 0x00, 0x01, 0x00, 0x50, 0x00, 0x50, 0x00, 0x08, 0x00, 0x00})
	f.Add([]byte{})
	f.Add([]byte{0x45, 0x00})
	f.Add(make([]byte, 2000))

	f.Fuzz(func(t *testing.T, payload []byte) {
		_, _ = parseIPv4UDP(payload)
	})
}

